public enum InstallationFileType {

    // ----------------------------------------------------------------------
    // Enums
    // ----------------------------------------------------------------------

    APK(".apk"),

    OBB(".obb"),

    PATCH(".obb");

    // ----------------------------------------------------------------------
    // Fields
    // ----------------------------------------------------------------------

    private String extesion;

    // ----------------------------------------------------------------------
    // Constructor
    // ----------------------------------------------------------------------

    InstallationFileType(String extension) {
        this.extesion = extension;
    }

    // ----------------------------------------------------------------------
    // Public methods
    // ----------------------------------------------------------------------

    public String getExtension() {
        return extesion;
    }

}
